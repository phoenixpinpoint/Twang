//operations.c

float add(float x, float y)
{
    float z = x + y;
    return z;
}
float sub(float x, float y)
{
    float z = x - y;
    return z;
}
float mul(float x, float y)
{
    float z = x * y;
    return z; 
}
float divide(float x, float y)
{
    float z = x / y;
    return z;   
}
